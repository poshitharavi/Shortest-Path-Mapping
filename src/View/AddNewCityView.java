    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        String cityName = cityNameTxt.getText().toString().trim();
        if(cityName == "" || cityName.equals("")){
            JOptionPane.showMessageDialog(this, "Please enter the city", "Error.", JOptionPane.ERROR_MESSAGE);  
        }else{
            if(CityController.searchExistingCity(cityName)){
                
                if(CityController.addNewCity(cityName)){
                    JOptionPane.showMessageDialog(this, "Save Successfully", "Success.", JOptionPane.INFORMATION_MESSAGE);  
                    this.loadAllCites();
                    cityNameTxt.setText("");
                }
            }else{
                JOptionPane.showMessageDialog(this, "Enterd city is excisting \n Try another city", "Error.", JOptionPane.ERROR_MESSAGE); 
            }
        }
    }//GEN-LAST:event_addBtnActionPerformed

