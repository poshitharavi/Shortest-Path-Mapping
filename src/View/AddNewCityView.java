    private void loadAllCites(){
        ArrayList<City> cityList = CityController.getAllCities();
        DefaultTableModel dtm = (DefaultTableModel)cityTbl.getModel();
         dtm.setRowCount(0);
         for(City city:cityList){
             dtm.addRow(new Object[]{city.getCityName()});
         }
    }

