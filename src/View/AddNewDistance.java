    public void loadCitiesToComboBox(){
        
        cityList = CityController.getAllCities();
        
        for(City city:cityList){
            fistCityCmb.addItem(city.getCityName());
            secCityCmb.addItem(city.getCityName());
        }
    }
    
    public City getSelectedCityinCmb(JComboBox<String> cmb){
        City selectedCity = null ;
        String key = cmb.getSelectedItem().toString();
        for(City city:cityList){
            
            if(city.getCityName().equals(key)){
               selectedCity = new City();
               selectedCity.setCityID(city.getCityID());
               selectedCity.setCityName(city.getCityName());
            }
        }
        return selectedCity;
    }

