    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        try{
        if(firstCity.getCityName().equals(secCity.getCityName()) || secCity.getCityName().equals(firstCity.getCityName()) ){
            JOptionPane.showMessageDialog(this, "Invalid City inputs try again", "Error.", JOptionPane.ERROR_MESSAGE);  
        }else if(distanceTxt.getText().toString().equals("")){
           JOptionPane.showMessageDialog(this, "Enter distance between two cites", "Error.", JOptionPane.ERROR_MESSAGE);  
        }else{
            
            if(DistanceController.getSelectedDistanceDetails(firstCity, secCity,Integer.parseInt(distanceTxt.getText().toString()))){
                
                DistanceController.addNewDistance(firstCity, secCity,Integer.parseInt(distanceTxt.getText().toString()));
                JOptionPane.showMessageDialog(this, "Distance saved successfully", "Success.", JOptionPane.INFORMATION_MESSAGE);
                distanceTxt.setText("");
                fistCityCmb.setSelectedIndex(0);
                secCityCmb.setSelectedIndex(0);
            }else{
                JOptionPane.showMessageDialog(this, "You have enterd the distance before", "Error.", JOptionPane.ERROR_MESSAGE);  
            }
        }
        }catch(Exception e){
             JOptionPane.showMessageDialog(this, "Enter both cites", "Error.", JOptionPane.ERROR_MESSAGE);  
        }
    }//GEN-LAST:event_addBtnActionPerformed

